word = "character"
--The # operator returns the length of a string.
print(#word)
name = "John is a "
-- The .. operator is for concatenation, somewhat similar to PHP
print(name .. word)
num = 10
--While normally subtraction, - can be used for negation on a single term
print(num)
print(-num)
--There is a direct exponentiation operator, ^
print(num^2)